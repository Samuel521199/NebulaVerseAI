import re


def clean_text(text):
        """уД╗жЎцТќЄТюгСИГуџёУАеТЃЁугдтЈи"""
        emoji_pattern = re.compile(
            "[\U0001F600-\U0001F64F]"  # УАеТЃЁугдтЈи (тдѓ­ЪўђсђЂ­Ъўѓ)
            "|[\U0001F300-\U0001F5FF]"  # угдтЈи & уџ«УѓцУ░ЃУЅ▓уЏў (тдѓРЏЁсђЂ­ЪјЅ)
            "|[\U0001F680-\U0001F6FF]"  # У┐љУЙЊ & тю░тЏЙ (тдѓ­ЪџђсђЂ­ЪџЌ)
            "|[\U0001F700-\U0001F77F]"  # жбЮтцќугдтЈи (тдѓ­ЪЏа№ИЈсђЂ­ЪДЕ)
            "|[\U0001F780-\U0001F7FF]"  # тЄаСйЋтйбуіХ (тдѓ­ЪЪасђЂ­Ъћи)
            "|[\U0001F800-\U0001F8FF]"  # жбЮтцќугдтЈи (тдѓ­ЪДХсђЂ­ЪЏњ)
            "|[\U0001F900-\U0001F9FF]"  # УАеТЃЁугдтЈиУАЦтЁЁ (тдѓ­ЪцЕсђЂ­ЪЦ│)
            "|[\U0001FA00-\U0001FAFF]"  # жбЮтцќугдтЈи (тдѓ­ЪЏќсђЂ­ЪЏЪ)
            "|[\u2600-\u26FF]"  # ТЮѓжА╣угдтЈи (тдѓРўђсђЂРџА)
            "|[\u2700-\u27BF]"  # ТЮѓжА╣угдтЈитњїу«Гтц┤ (тдѓРюѓсђЂРюћ)"
            "+", flags=re.UNICODE
        )
        return emoji_pattern.sub('', text)